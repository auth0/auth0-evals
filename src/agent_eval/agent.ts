import { estimateCost } from '../config/costs.js';
import { BedrockToolConfigError, LlmApiError } from '../errors.js';
import { BASE_URL, BEDROCK_MODELS, CLAUDE_EFFORT_MODELS, GEMINI_MODELS, MAX_TURNS } from '../config/settings.js';
import { TOOL_DEFINITIONS } from './tools/index.js';
import { collectFiles } from './tools/utils.js';
import { ToolExecutor } from './tools-executor/index.js';
import { ToolName } from './tools/base.js';

export function isBedrockModel(model: string): boolean {
  return BEDROCK_MODELS.some((prefix) => model.includes(prefix));
}

export function isGeminiModel(model: string): boolean {
  return GEMINI_MODELS.some((prefix) => model.startsWith(prefix));
}

// ── Data model ────────────────────────────────────────────────────────────────

export type FinishReason = 'tool_calls' | 'stop' | 'max_tokens' | 'length' | 'error' | 'unknown';
export type ErrorCategory = 'not_found' | 'timeout' | 'syntax' | 'auth' | 'network' | 'permission' | 'unknown';
export type ActionType = 'Implementation' | 'Discovery' | 'Error' | 'Interruption' | 'unknown';

/** Per-LLM-call metrics captured during the agent loop. */
export interface TurnMetric {
  /**
   * Turn number.
   *
   * @remarks Turn is 1-based for easier human interpretation (turn 1, turn 2, etc.) rather than 0-based indexing.
   */
  turn: number; // 1-based turn number
  /**
   * Number of tokens in the prompt sent to the LLM for this turn.
   */
  inputTokens: number;
  /**
   * Number of tokens in the response generated by the LLM for this turn.
   */
  outputTokens: number;
  /**
   * Time in seconds waiting for the LLM response for this turn,
   * measured from the moment the API call is made to the moment a response is received.
   *
   * @remarks This is wall-clock latency as experienced by the agent, and includes network time, LLM processing time, and any retries due to transient errors.
   * It does not include the time taken to execute tools or process the response after receiving it.
   * This metric is useful for understanding the responsiveness of the LLM and the overall agent loop.
   */
  llmLatency: number;
  /**
   * The reason why the LLM stopped generating a response in this turn.
   */
  finishReason: FinishReason;
  /**
   * The number of tool calls the LLM requested in this turn.
   *
   * @remarks This indicates how many times the agent asked to use a tool before generating a final response or stopping.
   * A higher number may indicate more complex reasoning or more attempts to gather information through tools.
   */
  toolCallCount: number;
  /**
   * Estimated cost for this turn
   */
  costUsd: number;
}

export interface ToolCallRecord {
  name: string;
  args: Record<string, unknown>;
  result: string;
  startTime: number;
  endTime: number;
  isDocLookup: boolean;
  isInterruption: boolean;
  causedError: boolean;
  /**
   * The type of action this tool call represents in the agent's reasoning process,
   * classified based on the tool name and whether it caused an error.
   */
  actionType: ActionType;
  /**
   * True if this tool call is a retry of a previous tool call with the same name and arguments that caused an error.
   */
  isRetry: boolean;
  /**
   * True if this tool call is a retry that successfully completed without error,
   * indicating that the agent recovered from a previous error by retrying the same action.
   */
  recoveredFromError: boolean;
  /**
   * If this tool call caused an error, the category of the error based on the error message.
   */
  errorCategory?: ErrorCategory;
}

export interface RunRecord {
  taskName: string;
  model: string;
  sessionId: string;
  startTime: number;
  endTime: number;
  toolCalls: ToolCallRecord[];
  turnMetrics: TurnMetric[];
  providerErrors: string[];
  inputTokens: number;
  outputTokens: number;
  costUsd: number;
  status: string;
  finalSummary: string;
  workspace: string;
}

function makeRunRecord(taskName: string, model: string, workspace: string): RunRecord {
  return {
    taskName,
    model,
    sessionId: Math.random().toString(36).slice(2, 10),
    startTime: 0,
    endTime: 0,
    toolCalls: [],
    turnMetrics: [],
    providerErrors: [],
    inputTokens: 0,
    outputTokens: 0,
    costUsd: 0,
    status: 'running',
    finalSummary: '',
    workspace,
  };
}

