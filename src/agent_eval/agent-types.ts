/**
 * Shared types and classification utilities used by all agent runners.
 *
 * Both react-agent and claude-code-agent produce a RunRecord consumed by the
 * scorer and reporting pipeline. All runner-agnostic types and helpers live
 * here so neither runner needs to import from the other.
 */

// ── Data model ────────────────────────────────────────────────────────────────

export type FinishReason = 'tool_calls' | 'stop' | 'max_tokens' | 'length' | 'error' | 'unknown';
export type ErrorCategory = 'not_found' | 'timeout' | 'syntax' | 'auth' | 'network' | 'permission' | 'unknown';
export type ActionType = 'Implementation' | 'Discovery' | 'Error' | 'Interruption' | 'unknown';

/** Per-LLM-call metrics captured during the agent loop. */
export interface TurnMetric {
  /**
   * Turn number.
   *
   * @remarks Turn is 1-based for easier human interpretation (turn 1, turn 2, etc.) rather than 0-based indexing.
   */
  turn: number; // 1-based turn number
  /**
   * Number of tokens in the prompt sent to the LLM for this turn.
   */
  inputTokens: number;
  /**
   * Number of tokens in the response generated by the LLM for this turn.
   */
  outputTokens: number;
  /**
   * Time in seconds waiting for the LLM response for this turn,
   * measured from the moment the API call is made to the moment a response is received.
   *
   * @remarks This is wall-clock latency as experienced by the agent, and includes network time, LLM processing time, and any retries due to transient errors.
   * It does not include the time taken to execute tools or process the response after receiving it.
   * This metric is useful for understanding the responsiveness of the LLM and the overall agent loop.
   */
  llmLatency: number;
  /**
   * The reason why the LLM stopped generating a response in this turn.
   */
  finishReason: FinishReason;
  /**
   * The number of tool calls the LLM requested in this turn.
   *
   * @remarks This indicates how many times the agent asked to use a tool before generating a final response or stopping.
   * A higher number may indicate more complex reasoning or more attempts to gather information through tools.
   */
  toolCallCount: number;
  /**
   * Estimated cost for this turn.
   */
  costUsd: number;
}

export interface ToolCallRecord {
  name: string;
  args: Record<string, unknown>;
  result: string;
  startTime: number;
  endTime: number;
  isDocLookup: boolean;
  isInterruption: boolean;
  causedError: boolean;
  /**
   * The type of action this tool call represents in the agent's reasoning process,
   * classified based on the tool name and whether it caused an error.
   */
  actionType: ActionType;
  /**
   * True if this tool call is a retry of a previous tool call with the same name and arguments that caused an error.
   */
  isRetry: boolean;
  /**
   * True if this tool call is a retry that successfully completed without error,
   * indicating that the agent recovered from a previous error by retrying the same action.
   */
  recoveredFromError: boolean;
  /**
   * If this tool call caused an error, the category of the error based on the error message.
   */
  errorCategory?: ErrorCategory;
}

export interface RunRecord {
  taskName: string;
  model: string;
  sessionId: string;
  startTime: number;
  endTime: number;
  toolCalls: ToolCallRecord[];
  turnMetrics: TurnMetric[];
  providerErrors: string[];
  inputTokens: number;
  outputTokens: number;
  costUsd: number;
  status: 'running' | 'success' | 'failure';
  finalSummary: string;
  workspace: string;
}

// ── Shared classification utilities ──────────────────────────────────────────

const TOOL_ACTION_TYPES: Record<string, ActionType> = {
  ask_user: 'Interruption',
  fetch_url: 'Discovery',
  read_file: 'Discovery',
  list_files: 'Discovery',
  write_file: 'Implementation',
  run_command: 'Implementation',
  finish_task: 'Implementation',
  search_auth0_docs: 'Discovery',
};

/**
 * Classify the type of action represented by a tool call based on its name and whether it caused an error.
 *
 * @param name The name of the tool being called.
 * @param causedError A boolean indicating whether the tool call resulted in an error.
 * @returns The classified ActionType for the tool call.
 */
export function classifyActionType(name: string, causedError: boolean): ActionType {
  if (causedError) {
    return 'Error';
  }

  // MCP tools are prefixed mcp__<server>__<tool> by Claude Code — treat as Discovery
  if (name.startsWith('mcp__')) {
    return 'Discovery';
  }

  return TOOL_ACTION_TYPES[name] ?? 'unknown';
}

/**
 * Extract the primary identifying argument from a tool call's name and arguments, which is used for retry detection.
 */
export function primaryArg(name: string, args: Record<string, unknown>): string {
  if (name === 'read_file' || name === 'list_files' || name === 'write_file') {
    return (args.path ?? args.filename ?? args.file_path ?? '') as string;
  }
  if (name === 'run_command') {
    return ((args.command as string) ?? '').slice(0, 80);
  }
  if (name === 'fetch_url') {
    return (args.url as string) ?? '';
  }
  if (name === 'ask_user') {
    return ((args.question as string) ?? '').slice(0, 80);
  }
  return JSON.stringify(args).slice(0, 80);
}

/**
 * Detect if the current tool call is a retry of a previous call that caused an error.
 *
 * @param toolCalls The history of tool calls to check against.
 * @param toolName The name of the tool being called.
 * @param toolArgs The arguments for the current tool call.
 * @returns True if the current call is a retry of a previous call that caused an error, otherwise false.
 */
export function detectRetry(toolCalls: ToolCallRecord[], toolName: string, toolArgs: Record<string, unknown>): boolean {
  const thisPrimary = primaryArg(toolName, toolArgs);
  const lastSame = toolCalls.findLast(
    (prev) => prev.name === toolName && primaryArg(prev.name, prev.args) === thisPrimary,
  );
  return lastSame?.causedError === true;
}

/**
 * Classify an error result string into a category.
 *
 * @param result  The error message string to classify.
 * @returns The category of the error.
 */
export function classifyErrorCategory(result: string): ErrorCategory {
  const r = result.toLowerCase();
  if (['not found', 'no such file', 'does not exist', 'file not found'].some((p) => r.includes(p))) return 'not_found';
  if (['timed out', 'timeout', 'deadline'].some((p) => r.includes(p))) return 'timeout';
  if (['permission denied', 'access denied', 'forbidden', '403'].some((p) => r.includes(p))) return 'permission';
  if (['401', 'unauthorized', 'unauthenticated'].some((p) => r.includes(p))) return 'auth';
  if (['connection', 'network', 'could not fetch', 'urlopen error', 'name or service'].some((p) => r.includes(p)))
    return 'network';
  if (['syntaxerror', 'syntax error', 'unexpected token', 'json', 'parse error', 'decode'].some((p) => r.includes(p)))
    return 'syntax';
  return 'unknown';
}
